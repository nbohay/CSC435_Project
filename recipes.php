            <h2 id="Kabobs">  Tequila-Lime Chicken and Vegetable Kabobs </h2>
            <img height="auto" width="65%" src="images/Chicken-Kabobs-Small.jpg"/>
            <p>Ingredients</p>
            <p>For the marinade: </p>
            <ul>
              <li>4 cloves garlic, minced </li>
              <li>1/4 cup olive oil</li>
              <li>2 tablespoons red wine vinegar </li>
              <li>2 tablespoons tequila (optional) </li>
              <li>Juice of 1 lime </li>
              <li>1 pound chicken, cut into bite-size pieces</li>
            </ul>
            <p>For the Vegetables: </p>
            <ul>
              <li>2 bell peppers, cut into 2-inch pieces </li>
              <li>1 zucchini, cut into 1/2-inch coins </li>
              <li>1 red onion, cut into 2-inch wedges </li>
              <li>2 tablespoons tallow, lard or ghee </li>
            </ul>
            <p>For the Sauce: </p>
            <ul>
              <li>2 cloves garlic </li>
              <li>1 cup chopped cilantro </li>
              <li>1 cup chopped parsley </li>
              <li>1/2 teaspoon sea salt</li>
              <li>1/8 teaspoon red pepper flakes (optional) </li>
              <li>Juice 1/2 lime </li>
              <li>1/2 cup olive oil</li>
            </ul>
            <p>Instructions</p>
            <ol>
              <li>Whisk garlic, olive oil, red wine vinegar, tequila and lime in a small measuring cup. Place chicken in a flat baking dish   and pour marinade overtop. Let marinate for 1 hour at room temperature or  you can cover and refrigerate for up to 4 hours. </li>
              <li>Preheat the grill to medium. Thread vegetables and chicken on stainless steel skewers. Brush with tallow, lard or ghee (this prevents the vegetables from sticking and helps the outside caramelize). Grill skewers 2-3 minutes per side until vegetables are golden on the edges and chicken is cooked through. </li>
              <li>Whisk all of the sauce ingredients together and serve with kabobs. </li>
              </ol>
